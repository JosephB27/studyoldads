#!/usr/bin/env node
/**
 * Regenerate src/data/imageDimensions.ts — a map of public image path to real
 * pixel dimensions. next/image needs intrinsic width/height to reserve exact
 * layout space (eliminating CLS) and to emit a correct responsive srcset.
 *
 * Run after adding or recropping any file under public/ads:
 *   node scripts/gen-image-dimensions.mjs
 *
 * Uses `sharp` if available, otherwise falls back to macOS `sips`.
 */
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative, sep } from "node:path";
import { execFileSync } from "node:child_process";

const ROOT = process.cwd();
const ADS_DIR = join(ROOT, "public", "ads");
const OUT = join(ROOT, "src", "data", "imageDimensions.ts");
const EXT = /\.(jpe?g|png|webp|avif|gif)$/i;

function walk(dir) {
  const files = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) files.push(...walk(full));
    else if (EXT.test(entry)) files.push(full);
  }
  return files;
}

function dimsViaSips(file) {
  const out = execFileSync("sips", ["-g", "pixelWidth", "-g", "pixelHeight", file], {
    encoding: "utf8",
  });
  const width = Number(out.match(/pixelWidth:\s*(\d+)/)?.[1]);
  const height = Number(out.match(/pixelHeight:\s*(\d+)/)?.[1]);
  return { width, height };
}

const files = walk(ADS_DIR).sort();
const entries = [];
for (const file of files) {
  const { width, height } = dimsViaSips(file);
  if (!width || !height) {
    console.warn(`WARN: could not read dimensions for ${file}`);
    continue;
  }
  const publicPath = "/" + relative(join(ROOT, "public"), file).split(sep).join("/");
  entries.push(`  ${JSON.stringify(publicPath)}: { width: ${width}, height: ${height} },`);
}

const banner = [
  "// AUTO-GENERATED by scripts/gen-image-dimensions.mjs — do not edit by hand.",
  "// Real pixel dimensions for every local ad image, so next/image can reserve",
  "// exact space (no layout shift) and emit a correct responsive srcset.",
  "export const imageDimensions: Record<string, { width: number; height: number }> = {",
].join("\n");

writeFileSync(OUT, `${banner}\n${entries.join("\n")}\n};\n`);
console.log(`Wrote ${entries.length} entries to ${relative(ROOT, OUT)}`);
